        }



        public static string TimeSpanSub(string str)
        {
            try
            {
                return TimeSpanSub(DateTime.Parse(str));
            }
            catch (Exception ex)
            {
                XTrace.WriteLine(ex.Message + ex.StackTrace);
                return "";
            }
        }
        public static string TimeSpanSub(object obj)
        {
            try
            {
                return TimeSpanSub(obj.ToString());
            }
            catch (Exception ex)
            {
                XTrace.WriteLine(ex.Message + ex.StackTrace);
                return "";
            }
        }
        public static string TimeSpanSub(DateTime dt)
        {
            StringBuilder str = new StringBuilder();


            TimeSpan ts = DateTime.Now.Subtract(dt);
            if (ts.Days > 0)
            {
                str.Append(ts.Days + "天");
            }
            if (ts.Hours > 0)
            {
                str.Append(ts.Hours + "小时");
            }
            if (ts.Minutes > 0)
            {
                str.Append(ts.Minutes + "分");
            }
            if (ts.Seconds > 0)
            {
                str.Append(ts.Seconds + "秒");
            }
            return str.ToString();
            //Response.Write(ts.Days + "天" + ts.Hours + "小时" + ts.Minutes + "分" + ts.Seconds + "秒");
        }

